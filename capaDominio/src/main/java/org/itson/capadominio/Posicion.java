/**
 * El enum Posicion representa las posibles posiciones de un elemento en un contexto determinado.
 * Puede ser uno de los siguientes valores: VERTICAL o HORIZONTAL.
 */
public enum Posicion {
    /**
     * Representa una posición vertical en un contexto determinado.
     */
    VERTICAL,

    /**
     * Representa una posición horizontal en un contexto determinado.
     */
    HORIZONTAL;
}
